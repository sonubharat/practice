# Original tuple
my_tuple = ("apple", "banana", "cherry", "orange", "mango")

# Slicing the tuple (start index, stop index, step)
sliced_tuple = my_tuple[1:4]  # Extract elements from index 1 (inclusive) to 4 (exclusive)

# Print the original and sliced tuple
print("Original tuple:", my_tuple)
print("Sliced tuple:", sliced_tuple)
